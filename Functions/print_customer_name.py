# You're managing a tea shop. You receive mane orders and want
# to print each customer's name along with the type of tea ordered.
# Taks:
# 1. Write a function print_order(name, tea_type)
# 2. Call it multiple times for different customers.

def print_order(name, tea_type):
    print(f"{name} ordered {tea_type}")

print_order("Aman", "Masala")
print_order("Jia", "Ginger")